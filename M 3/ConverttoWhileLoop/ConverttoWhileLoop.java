public final class ConverttoWhileLoop {
    /**
    Do not modify this class
    */
    /**
    @default constructor
    */
    private ConverttoWhileLoop() { }
    /**
    @param  args string
    */
    public static void main(final String[] args) {
        int i = 2;
        final int br = 10;
        while (i <= br) {
            System.out.println(i);
            i = i + 2;
        }
        System.out.println("GoodBye!");
    }
}
